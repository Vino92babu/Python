'''
A comparison operator in python, also called python relational operators are used to establish some sort
of relationship between the two operands.
Some of the relevant examples could be less than, greater than or equal to operators.
Relational operators compares the values of two operands and returns
TRUE or FALSE based on whether the condition is met.
'''
# Comparison Operators or Relational Operators
"""
==	Equal
!=	Not equal
>	Greater than
<	Less than
>=	Greater than or equal to
<=	Less than or equal to

"""
a = 20
b = 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)